"""
Run Qwen3-ASR prediction on a JSONL file, compute WER/CER,
and write a new JSONL file.

Default input format:
{
  "index": 1755,
  "audio_path": "examples/noise.wav",
  "question": "Please transcribe the audio content into text.",
  "answer": "I usually take the quieter road home because the main street gets crowded after work.",
  "subset": "sim-en-noise",
  "name": "vitw_01756_random_synthetic_en_noise"
}

Required input fields:
- audio_path: input audio file path
- answer: reference transcription

Optional input fields:
- index
- question
- subset
- name
- other metadata fields

Default output:
- keep all original fields
- add/update:
  - prediction: transcription generated by Qwen3-ASR
  - pred_language: language detected by Qwen3-ASR
  - wer: WER for English samples, CER for Chinese samples
  - metric: "wer" for English, "cer" for Chinese
  - num_edits
  - ref_len
"""

