LANG_PREFIX_RE = re.compile(r"^\s*language\s+[^<\s]+<asr_text>", flags=re.IGNORECASE)


@contextlib.contextmanager
def maybe_suppress_stderr(verbose: bool = False):
    if verbose:
        yield
    else:
        with open(os.devnull, "w") as devnull:
            old_stderr = sys.stderr
            sys.stderr = devnull
            try:
                yield
            finally:
                sys.stderr = old_stderr


def strip_asr_prefix(text: Any) -> str:
    """Remove 'language English<asr_text>'-style prefix."""
    if text is None:
        return ""
    text = str(text).strip()
    if "<asr_text>" in text:
        text = text.split("<asr_text>", 1)[1]
    text = LANG_PREFIX_RE.sub("", text)
    return text.strip()


def has_chinese(text: str) -> bool:
    return re.search(r"[\u4e00-\u9fff]", text or "") is not None


def detect_language(item: Dict[str, Any], ref: str, pred: str, force_language: str) -> str:
    """
    Return 'en' or 'zh'.

    force_language:
      - auto: detect by fields and Chinese characters
      - en: force English WER
      - zh: force Chinese CER
    """
    if force_language in {"en", "zh"}:
        return force_language

    probe = " ".join(
        str(item.get(k, ""))
        for k in ["language", "question", "answer", "text", "prediction", "pred_language"]
    )
    probe = f"{probe} {ref} {pred}"
    probe_lower = probe.lower()

    if (
        "chinese" in probe_lower
        or "mandarin" in probe_lower
        or "cantonese" in probe_lower
        or "zh" in probe_lower
        or "中文" in probe
        or "汉语" in probe
        or has_chinese(probe)
    ):
        return "zh"

    return "en"


def remove_punctuation_keep_space(text: str) -> str:
    out = []
    for ch in text:
        if unicodedata.category(ch).startswith("P"):
            out.append(" ")
        else:
            out.append(ch)
    return "".join(out)


class WERCERScorer:
    def __init__(self):
        self.en_normalizer = EnglishTextNormalizer() if EnglishTextNormalizer else None
        self.zh_normalizer = TextNorm() if TextNorm else None

    def normalize_english(self, text: str) -> str:
        text = strip_asr_prefix(text)
        if self.en_normalizer is not None:
            try:
                text = self.en_normalizer(text)
            except Exception:
                pass
        text = unicodedata.normalize("NFKC", text)
        text = text.lower()
        text = remove_punctuation_keep_space(text)
        text = " ".join(text.split())
        return text

    def normalize_chinese(self, text: str) -> str:
        text = strip_asr_prefix(text)
        text = unicodedata.normalize("NFKC", text)
        if self.zh_normalizer is not None:
            try:
                text = self.zh_normalizer(text)
            except Exception:
                pass
        text = remove_punctuation_keep_space(text)
        text = re.sub(r"\s+", "", text)
        return text

    @staticmethod
    def _distance(ref_tokens: List[str], hyp_tokens: List[str]) -> int:
        if _editdistance is not None:
            return int(_editdistance.eval(ref_tokens, hyp_tokens))

        # Fallback Levenshtein DP.
        n, m = len(ref_tokens), len(hyp_tokens)
        dp = list(range(m + 1))
        for i in range(1, n + 1):
            prev = dp[0]
            dp[0] = i
            for j in range(1, m + 1):
                cur = dp[j]
                cost = 0 if ref_tokens[i - 1] == hyp_tokens[j - 1] else 1
                dp[j] = min(dp[j] + 1, dp[j - 1] + 1, prev + cost)
                prev = cur
        return dp[m]

    def score(self, ref: str, pred: str, lang: str) -> Tuple[float, int, int, str, str, str]:
        if lang == "zh":
            ref_norm = self.normalize_chinese(ref)
            pred_norm = self.normalize_chinese(pred)
            ref_tokens = list(ref_norm)
            pred_tokens = list(pred_norm)
            metric = "cer"
        else:
            ref_norm = self.normalize_english(ref)
            pred_norm = self.normalize_english(pred)
            ref_tokens = ref_norm.split() if ref_norm else []
            pred_tokens = pred_norm.split() if pred_norm else []
            metric = "wer"

        ref_len = len(ref_tokens)
        dist = self._distance(ref_tokens, pred_tokens)

        if ref_len == 0:
            err = 0.0 if len(pred_tokens) == 0 else 1.0
        else:
            err = dist / ref_len

        return err, dist, ref_len, metric, ref_norm, pred_norm


def infer_data_format(item: Dict[str, Any], data_format: str) -> str:
    if data_format in {"v1", "v2"}:
        return data_format
    if "audio" in item and "text" in item:
        return "v2"
    return "v1"


def get_audio_value(item: Dict[str, Any], fmt: str, audio_key: str) -> str:
    if audio_key:
        return str(item.get(audio_key, ""))
    if fmt == "v2":
        return str(item.get("audio", ""))
    return str(item.get("audio_path", ""))


def get_ref_value(item: Dict[str, Any], fmt: str, ref_key: str) -> str:
    if ref_key:
        return strip_asr_prefix(item.get(ref_key, ""))
    if fmt == "v2":
        return strip_asr_prefix(item.get("text", ""))
    return strip_asr_prefix(item.get("answer", ""))


def resolve_audio_path(raw_path: str, input_jsonl: str, audio_root: str = "") -> Optional[str]:
    if not raw_path:
        return None

    p = Path(raw_path)
    if p.is_absolute() and p.exists():
        return str(p)

    candidates = []
    if audio_root:
        candidates.append(Path(audio_root) / raw_path)

    # JSONL parent is useful when paths are relative to the metadata file.
    candidates.append(Path(input_jsonl).resolve().parent / raw_path)

    # CWD is useful when running from the project root.
    candidates.append(Path.cwd() / raw_path)

    for c in candidates:
        if c.exists():
            return str(c.resolve())

    # Return the most likely candidate for debugging.
    if candidates:
        return str(candidates[0].resolve())
    return str(p)


def split_chunks(data: List[Tuple[int, Dict[str, Any]]], n: int) -> List[List[Tuple[int, Dict[str, Any]]]]:
    return [data[i::n] for i in range(n)]


def worker(
    worker_id: int,
    gpu_id: str,
    data_chunk: List[Tuple[int, Dict[str, Any]]],
    args_dict: Dict[str, Any],
    out_q: Queue,
):
    try:
        # Must set CUDA_VISIBLE_DEVICES before importing torch/qwen_asr in this process.
        if gpu_id.lower() not in {"cpu", "-1"}:
            os.environ["CUDA_VISIBLE_DEVICES"] = str(gpu_id)

        with maybe_suppress_stderr(args_dict["verbose"]):
            import torch
            from qwen_asr import Qwen3ASRModel

            try:
                from transformers.utils import logging as hf_logging
                hf_logging.set_verbosity_error()
                hf_logging.disable_progress_bar()
            except Exception:
                pass

        device_map = "cpu" if gpu_id.lower() in {"cpu", "-1"} else "cuda:0"
        dtype = torch.float32 if device_map == "cpu" else torch.bfloat16

        with maybe_suppress_stderr(args_dict["verbose"]):
            model = Qwen3ASRModel.from_pretrained(
                args_dict["model_path"],
                dtype=dtype,
                device_map=device_map,
                max_inference_batch_size=args_dict["batch_size"],
                max_new_tokens=args_dict["max_new_tokens"],
            )

        # Optional LoRA adapter.
        adapter_dir = args_dict.get("adapter_dir") or ""
        if adapter_dir and os.path.exists(adapter_dir):
            with maybe_suppress_stderr(args_dict["verbose"]):
                from peft import PeftModel

                base_model = model.model
                if not isinstance(base_model.thinker, PeftModel):
                    base_model.thinker = PeftModel.from_pretrained(
                        base_model.thinker,
                        adapter_dir,
                        is_trainable=False,
                    )
                else:
                    base_model.thinker.load_adapter(
                        adapter_dir,
                        adapter_name="default",
                        is_trainable=False,
                    )
                base_model.thinker.set_adapter("default")

        scorer = WERCERScorer()

        batch_size = int(args_dict["batch_size"])
        input_jsonl = args_dict["input_jsonl"]

        for start in tqdm(
            range(0, len(data_chunk), batch_size),
            desc=f"worker {worker_id}/gpu {gpu_id}",
            position=worker_id,
            leave=True,
        ):
            pairs = data_chunk[start : start + batch_size]

            valid_pairs = []
            audio_paths = []

            # First resolve valid audio paths.
            for line_idx, item in pairs:
                fmt = infer_data_format(item, args_dict["data_format"])
                raw_audio = get_audio_value(item, fmt, args_dict["audio_key"])
                resolved_audio = resolve_audio_path(
                    raw_audio,
                    input_jsonl=input_jsonl,
                    audio_root=args_dict["audio_root"],
                )

                if not resolved_audio or not os.path.exists(resolved_audio):
                    item["prediction"] = item.get("prediction", "")
                    item["error"] = f"audio_not_found: {raw_audio}"
                    out_q.put((line_idx, item))
                    continue

                if args_dict["write_resolved_audio_path"]:
                    item["resolved_audio_path"] = resolved_audio

                valid_pairs.append((line_idx, item))
                audio_paths.append(resolved_audio)

            if not valid_pairs:
                continue

            # Run Qwen3-ASR batch inference.
            with maybe_suppress_stderr(args_dict["verbose"]):
                results = model.transcribe(audio=audio_paths, language=None)

            if not isinstance(results, list):
                results = [results]

            for (line_idx, item), res_obj in zip(valid_pairs, results):
                fmt = infer_data_format(item, args_dict["data_format"])
                ref = get_ref_value(item, fmt, args_dict["ref_key"])

                pred = getattr(res_obj, "text", None)
                if pred is None:
                    pred = str(res_obj)
                pred = str(pred).strip()

                pred_language = getattr(res_obj, "language", "")
                item["prediction"] = pred
                item["pred_language"] = str(pred_language)

                lang = detect_language(
                    item=item,
                    ref=ref,
                    pred=pred,
                    force_language=args_dict["language"],
                )

                err, dist, ref_len, metric, ref_norm, pred_norm = scorer.score(ref, pred, lang)

                # Keep 'wer' as the default score field for compatibility.
                # If metric == 'cer', this field stores CER.
                item[args_dict["score_field"]] = round(float(err), 6)
                item["metric"] = metric
                item["num_edits"] = int(dist)
                item["ref_len"] = int(ref_len)

                if args_dict["keep_norm_text"]:
                    item["ref_norm"] = ref_norm
                    item["pred_norm"] = pred_norm

                out_q.put((line_idx, item))

        out_q.put(("DONE", worker_id))

    except Exception:
        out_q.put(("WORKER_ERROR", worker_id, traceback.format_exc()))


def main():
    parser = argparse.ArgumentParser(
        description="Run Qwen3-ASR prediction and compute WER/CER for JSONL."
    )
    parser.add_argument("--input_jsonl", type=str, required=True)
    parser.add_argument("--output_jsonl", type=str, required=True)

    parser.add_argument(
        "--data_format",
        type=str,
        default="auto",
        choices=["auto", "v1", "v2"],
        help="v1: audio_path+answer; v2: audio+text; auto: infer from keys.",
    )
    parser.add_argument("--audio_key", type=str, default="", help="Override audio key.")
    parser.add_argument("--ref_key", type=str, default="", help="Override reference key.")
    parser.add_argument(
        "--audio_root",
        type=str,
        default="",
        help="Root directory for relative audio paths such as examples/noise.wav.",
    )

    parser.add_argument("--model_path", type=str, default=" ckpt/Mega-ASR/Qwen3-ASR-1.7B")
    parser.add_argument("--adapter_dir", type=str, default="")
    parser.add_argument("--gpus", type=str, default="0", help='Comma-separated GPU ids, e.g. "0" or "0,1,2,3". Use "cpu" for CPU.')
    parser.add_argument("--batch_size", type=int, default=8)
    parser.add_argument("--max_new_tokens", type=int, default=256)

    parser.add_argument(
        "--language",
        type=str,
        default="auto",
        choices=["auto", "en", "zh"],
        help="auto: English WER / Chinese CER; en: force WER; zh: force CER.",
    )
    parser.add_argument("--score_field", type=str, default="wer")
    parser.add_argument("--keep_norm_text", action="store_true")
    parser.add_argument("--write_resolved_audio_path", action="store_true")
    parser.add_argument("--verbose", action="store_true")

    args = parser.parse_args()

    input_path = Path(args.input_jsonl)
    output_path = Path(args.output_jsonl)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    data: List[Tuple[int, Dict[str, Any]]] = []
    with input_path.open("r", encoding="utf-8") as f:
        for line_idx, line in enumerate(f):
            line = line.strip()
            if not line:
                continue
            data.append((line_idx, json.loads(line)))

    if not data:
        raise ValueError(f"No valid JSONL lines found in {input_path}")

    gpu_ids = [x.strip() for x in args.gpus.split(",") if x.strip()]
    if not gpu_ids:
        gpu_ids = ["0"]

    args_dict = vars(args).copy()
    args_dict["input_jsonl"] = str(input_path)

    chunks = split_chunks(data, len(gpu_ids))
    out_q: Queue = Queue(maxsize=1024)

    procs: List[Process] = []
    t0 = time.time()

    for worker_id, gpu_id in enumerate(gpu_ids):
        p = Process(
            target=worker,
            args=(worker_id, gpu_id, chunks[worker_id], args_dict, out_q),
        )
        p.start()
        procs.append(p)

    results: Dict[int, Dict[str, Any]] = {}
    finished = 0

    pbar = tqdm(total=len(data), desc="collecting")
    while finished < len(procs):
        try:
            msg = out_q.get(timeout=5)
        except queue.Empty:
            # If any worker died unexpectedly, stop early with a clear error.
            dead = [p for p in procs if (not p.is_alive() and p.exitcode not in (0, None))]
            if dead:
                raise RuntimeError(f"Worker died unexpectedly: {[p.exitcode for p in dead]}")
            continue

        if isinstance(msg, tuple) and len(msg) >= 1 and msg[0] == "DONE":
            finished += 1
            continue

        if isinstance(msg, tuple) and len(msg) >= 1 and msg[0] == "WORKER_ERROR":
            _, worker_id, err = msg
            raise RuntimeError(f"Worker {worker_id} failed:\n{err}")

        line_idx, item = msg
        results[int(line_idx)] = item
        pbar.update(1)

    pbar.close()

    for p in procs:
        p.join()

    # Preserve the original input order.
    with output_path.open("w", encoding="utf-8") as f:
        for line_idx, _ in data:
            item = results.get(line_idx)
            if item is None:
                # Should not happen, but keep an explicit trace.
                item = {"error": f"missing_result_for_line_{line_idx}"}
            f.write(json.dumps(item, ensure_ascii=False) + "\n")

    elapsed = time.time() - t0
    print(f"[done] processed {len(data)} samples")
    print(f"[output] {output_path}")
    print(f"[elapsed] {elapsed:.2f}s")


if __name__ == "__main__":
    try:
        set_start_method("spawn")
    except RuntimeError:
        pass
    main()
